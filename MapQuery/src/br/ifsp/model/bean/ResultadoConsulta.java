package br.ifsp.model.bean;

public class ResultadoConsulta {
	public String sumRenevue;
	public String brand;
	public String year;
	
	public String getSumRenevue() 
	{
		return sumRenevue;
	}
	public void setSumRenevue(String sumRenevue) 
	{
		this.sumRenevue = sumRenevue;
	}
	
	
	public String getBrand() 
	{
		return brand;
	}
	public void setBrand(String brand) 
	{
		this.brand = brand;
	}
	
	
	public String getYear() 
	{
		return year;
	}
	public void setYear(String year) 
	{
		this.year = year;
	}
	
	
}

